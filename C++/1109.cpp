#include <iostream>
using namespace std;


int main()
{
    int num;

    cin >> num;

    cout << "The next number for the number " << num
        << " is " << num + 1 << "." << endl;
    cout << "The previous number for the number " << num
        << " is " << num - 1 << "." << endl;

    return 0;
}
